// Helper functions
export const getTreksByDifficulty = (difficulty) => {
  return treks.filter(trek => trek.difficulty.toLowerCase().includes(difficulty.toLowerCase()));
};

export const getTreksByState = (stateId) => {
  return treks.filter(trek => trek.stateId === stateId);
};

export const getTreksByCity = (cityId) => {
  return treks.filter(trek => trek.cityId === cityId);
};

export const getTrekById = (id) => {
  return treks.find(trek => trek.id === parseInt(id));
};

export const getTreksBySeason = (season) => {
  return treks.filter(trek => trek.bestSeason.toLowerCase().includes(season.toLowerCase()));
};
