// Helper functions
export const getTemplesByCategory = (category) => {
  return allTemples.filter(temple => temple.category === category);
};

export const getTemplesByState = (stateId) => {
  return allTemples.filter(temple => temple.stateId === stateId);
};

export const getTemplesByCity = (cityId) => {
  return allTemples.filter(temple => temple.cityId === cityId);
};

export const getTempleById = (id) => {
  return allTemples.find(temple => temple.id === parseInt(id));
};
