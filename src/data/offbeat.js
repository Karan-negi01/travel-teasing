// Helper functions
export const getOffbeatPlacesByState = (stateId) => {
  return offbeatPlaces.filter(place => place.stateId === stateId);
};

export const getOffbeatPlacesByCity = (cityId) => {
  return offbeatPlaces.filter(place => place.cityId === cityId);
};

export const getOffbeatPlacesByType = (type) => {
  return offbeatPlaces.filter(place => place.type.toLowerCase().includes(type.toLowerCase()));
};

export const getOffbeatPlaceById = (id) => {
  return offbeatPlaces.find(place => place.id === parseInt(id));
};
